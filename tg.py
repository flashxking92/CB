@client.on(events.NewMessage(pattern=r'^\.approve$'))
async def approve_user(event):
    if event.sender_id != OWNER_ID:
        return await event.reply("❌ Only the owner can approve users.")
    if event.is_reply:
        user = await event.get_reply_message()
        approved_users.add(user.sender_id)
        await event.reply(f"✅ Approved [{user.sender.first_name}](tg://user?id={user.sender_id})")
    else:
        await event.reply("⚠️ Reply to a user's message to approve them.")

@client.on(events.NewMessage(pattern=r'^\.disapprove$'))
async def disapprove_user(event):
    if event.sender_id != OWNER_ID:
        return await event.reply("❌ Only the owner can disapprove users.")
    if event.is_reply:
        user = await event.get_reply_message()
        approved_users.discard(user.sender_id)
        await event.reply(f"🚫 Disapproved [{user.sender.first_name}](tg://user?id={user.sender_id})")
    else:
        await event.reply("⚠️ Reply to a user's message to disapprove them.")

@client.on(events.NewMessage(pattern=r'^\.target(?:\s+@?(\w+))?$'))
async def set_target(event):
    global target_user_id
    if event.sender_id != OWNER_ID and event.sender_id not in approved_users:
        return await event.reply("❌ You are not approved to use this bot.")

    if event.is_reply:
        replied = await event.get_reply_message()
        target_user_id = replied.sender_id
        name = replied.sender.first_name if replied.sender else "Unknown"
        await event.reply(f"🎯 Target set to [{name}](tg://user?id={target_user_id})")
    elif event.pattern_match.group(1):
        username = event.pattern_match.group(1)
        try:
            user = await client.get_entity(username)
            target_user_id = user.id
            await event.reply(f"🎯 Target set to [{user.first_name}](tg://user?id={target_user_id})")
        except Exception:
            await event.reply("❌ Could not find user by username.")
    else:
        await event.reply("⚠️ Reply to a message or use `.target @username` to set target.")

@client.on(events.NewMessage(pattern=r'^\.remove$'))
async def remove_target(event):
    global target_user_id
    if event.sender_id != OWNER_ID and event.sender_id not in approved_users:
        return await event.reply("❌ You are not approved to use this bot.")
    target_user_id = None
    await event.reply("✅ Target removed. No more abuses.")

@client.on(events.NewMessage)
async def auto_abuse(event):
    global target_user_id
    if target_user_id is None:
        return
    if event.sender_id == target_user_id and not event.out:
        await event.reply(random.choice(abuse_list))

